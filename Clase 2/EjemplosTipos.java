public class EjemplosTipos {
    public static void main(String[] args){

        // Tipos enteros
        int entero = 12;
        byte bits8 = 1;
        long enteroGrande = 100000L;
        // Tipos decimales
        float pi = 3.14f;
        double piMas = 3.14159;
        // Caracteres
        char caracter = 'a';
        String palabra = "Hola Mundo";
        // Booleanos
        boolean esVerdadero = true;

        System.out.println(entero + ", " + bits8 + ", " + 
                           enteroGrande + ", " + pi + ", " + piMas + ", " + caracter + ", " + 
                           palabra + ", " + esVerdadero);
    }
}